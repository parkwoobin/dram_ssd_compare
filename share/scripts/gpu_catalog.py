def iter_models(catalog: "dict[str, dict[str, int | None]]"):
    """(시리즈, 표시명, gpuId) 를 카탈로그 순서대로 순회."""
    for series, models in catalog.items():
        for name, gpu_id in models.items():
            yield series, name, gpu_id


def count_models(catalog: "dict[str, dict[str, int | None]]") -> int:
    return sum(len(m) for m in catalog.values())
