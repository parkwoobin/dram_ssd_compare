# 전체 수집 실행 (desktop / laptop 공통)
# ---------------------------------------------------------------------------
async def run_scrape(catalog: "dict[str, dict[str, int | None]]", kind: str,
                     bench: str = "timespy", *,
                     series_filter: "list[str] | None" = None,
                     min_delay: float = 1.0, max_delay: float = 3.0,
                     mode: str = "auto"):
    """
    카탈로그를 순회하며 각 GPU 의 Graphics score 평균/결과 수를 수집해 xlsx 로 저장.
    bench 는 BENCHMARKS 의 키(timespy/firestrike/portroyal/steelnomad).
    - GPU 사이에 무작위 지연(min_delay~max_delay 초) 을 둬 차단을 예방한다.
    - 차단(BlockedError) 감지 시 즉시 중단하되, 그때까지 수집분은 저장한다.
    - 진행 기록(output/{kind}/{bench}/_progress.json)을 남겨, 차단·중단 후 다시
      실행하면 이미 수집한 GPU 는 건너뛰고 이어서 수집한다. 전체 완료 시 삭제된다.
    반환: (저장경로, 차단여부)
    """
    import json
    import random
    import time
    from playwright.async_api import async_playwright

    bench_name, test_code = BENCHMARKS[bench]
    log = get_logger(kind, bench)
    log.info(f"벤치마크: {bench_name} (test={test_code})")
    progress_path = bench_dir(kind, bench) / "_progress.json"

    # 이전 진행 기록 로드 (단일 실행 내 재개)
    progress: "dict[str, list]" = {}
    if progress_path.exists():
        try:
            progress = json.loads(progress_path.read_text(encoding="utf-8"))
        except Exception:  # noqa: BLE001
            progress = {}
    if progress:
        log.info(f"이전 진행 기록 {len(progress)}개 발견 — 이어서 수집합니다.")

    def save_progress():
        progress_path.write_text(json.dumps(progress, ensure_ascii=False), encoding="utf-8")

    # 모드 결정: auto = 최근(24h 이내) 결과가 있으면 증분 업데이트(신규 GPU만)
    existing, mtime = load_latest_result(kind, bench)
    if mode == "auto":
        if existing and mtime and (time.time() - mtime < 24 * 3600):
            mode = "update"
            hrs = (time.time() - mtime) / 3600
            log.info(f"최근 수집({hrs:.1f}시간 전, {len(existing)}개)이 24시간 이내 "
                     f"→ 증분 업데이트 모드(신규 GPU만 수집, 기존 데이터 유지)")
        else:
            mode = "full"
    # update 모드: 기존 xlsx 의 gpuId 데이터를 재사용(스킵) 대상으로 둔다.
    #   단, 점수 수집에 '실패'(응답 없음 → count 도 None)한 GPU 는 known 에서 제외해
    #   최근에 시도했더라도 다시 수집하게 한다. (count==0 인 '결과 없음'은 정상이라 유지)
    known: "dict[int, tuple]" = {}
    if mode == "update":
        known = {gid: (avg, count) for gid, (s, m, avg, count, u) in existing.items()
                 if count is not None}
        skipped_fail = sum(1 for gid, (s, m, avg, count, u) in existing.items() if count is None)
        if skipped_fail:
            log.info(f"이전 수집 실패 {skipped_fail}개 — 이번에 다시 시도합니다.")
    else:
        log.info("전체 수집 모드")

    rows: "list[Row]" = []
    total = sum(len(m) for s, m in catalog.items()
                if not series_filter or s in series_filter)
    log.info(f"수집 시작: {kind}  대상 {total}개 GPU"
             + (f"  (시리즈 필터: {series_filter})" if series_filter else ""))

    done = 0
    fetched = 0          # 이번 실행에서 실제 네트워크로 가져온 수
    blocked = False
    async with async_playwright() as p:
        browser = await launch_browser(p, log)
        ctx = await make_context(browser)
        page = await ctx.new_page()
        try:
            # 세션 확보(쿠키/Origin): 검색 페이지를 1회만 로드한다.
            # 이후 각 GPU 는 이 페이지에서 통계 API(ajax)만 호출한다.
            try:
                resp = await page.goto(
                    "https://www.3dmark.com/search#advanced?test="
                    + test_code.replace(" ", "%20"),
                    wait_until="domcontentloaded", timeout=30000)
                if resp is not None and resp.status in (403, 429, 503):
                    raise BlockedError(f"HTTP {resp.status}")
                if _BLOCK_RE.search((await page.content())[:1500]):
                    raise BlockedError("차단 페이지 감지")
                await page.wait_for_timeout(1500)
                log.info("세션 확보 완료 — 통계 API 직접 호출 모드")
            except BlockedError as e:
                log.error(f"!! 시작 페이지 로드 중 차단({e}). 잠시 후 다시 실행하세요.")
                blocked = True

            for series, models in catalog.items():
                if blocked:
                    break
                if series_filter and series not in series_filter:
                    continue
                for name, gpu_id in models.items():
                    done += 1
                    if gpu_id is None:
                        log.info(f"[{done}/{total}] {series} / {name} — gpuId 미등록, 건너뜀")
                        rows.append((series, name, None, None, ""))
                        continue

                    key = str(gpu_id)
                    # 이미 수집된 항목은 재사용 (단일 실행 내 재개)
                    if key in progress:
                        avg, count = progress[key]
                        rows.append((series, name, avg, count, build_url(gpu_id, test_code)))
                        log.info(f"[{done}/{total}] {series} / {name} (id={gpu_id})"
                                 f"  →  (재개) 평균={avg}  결과수={count}")
                        continue
                    # 증분 업데이트: 기존 xlsx 에 있던 GPU 는 그대로 두고 건너뛴다
                    if gpu_id in known:
                        avg, count = known[gpu_id]
                        rows.append((series, name, avg, count, build_url(gpu_id, test_code)))
                        log.info(f"[{done}/{total}] {series} / {name} (id={gpu_id})"
                                 f"  →  (기존 유지) 평균={avg}  결과수={count}")
                        continue

                    try:
                        avg, count, url = await fetch_score(page, gpu_id, test_code, log=log)
                    except BlockedError as e:
                        log.error(f"!! CloudFront/WAF 차단 감지({e}). 수집을 중단합니다.")
                        log.error("   잠시(수십 분~수 시간) 후 다시 실행하면 "
                                  "이어서 수집합니다. 또는 --min-delay/--max-delay 를 늘리세요.")
                        blocked = True
                        break

                    rows.append((series, name, avg, count, url))
                    log.info(f"[{done}/{total}] {series} / {name} (id={gpu_id})"
                             f"  →  평균={avg}  결과수={count}")
                    # 성공/0건만 기록(완전 실패는 다음 실행에서 재시도)
                    if avg is not None or count is not None:
                        progress[key] = [avg, count]
                        save_progress()
                    fetched += 1
                    # GPU 사이 무작위 지연
                    await asyncio.sleep(random.uniform(min_delay, max_delay))
                if blocked:
                    break
        finally:
            await browser.close()

    path = save_xlsx(rows, kind, bench)
    suffix = "  [차단으로 중단됨]" if blocked else ""
    ok = sum(1 for r in rows if r[2] is not None)
    log.info(f"저장 완료: {path}")
    log.info(f"총 {len(rows)}행 (점수 확보 {ok}개, 이번 실행 신규 {fetched}개){suffix}")

    if blocked:
        log.info(f"진행 기록 보관됨({len(progress)}개). 다시 실행하면 이어서 수집합니다.")
    else:
        progress_path.unlink(missing_ok=True)
        log.info("전체 수집 완료 — 진행 기록을 초기화했습니다.")
    return path, blocked


# ---------------------------------------------------------------------------
# Excel 저장
# ---------------------------------------------------------------------------
def save_xlsx(rows: "list[Row]", kind: str, bench: str = "timespy") -> Path:
    """
    rows 를 output/{kind}/{bench}/ 에 .xlsx 로 저장한다.
    컬럼: 시리즈 / 모델명 / 평균 점수 / 총 결과 수 / 링크
    """
    from openpyxl import Workbook
    from openpyxl.styles import Alignment, Font, PatternFill
    from openpyxl.utils import get_column_letter

    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    d = bench_dir(kind, bench)
    path = d / f"3dmark_{kind}_{bench}_scores_{ts}.xlsx"

    wb = Workbook()
    ws = wb.active
    ws.title = "Time Spy Graphics"

    headers = ["시리즈", "모델명", "평균 점수", "총 결과 수", "링크"]
    ws.append(headers)

    header_font = Font(bold=True, color="FFFFFF")
    header_fill = PatternFill("solid", fgColor="305496")
    for col, _ in enumerate(headers, start=1):
        c = ws.cell(row=1, column=col)
        c.font = header_font
        c.fill = header_fill
        c.alignment = Alignment(horizontal="center", vertical="center")

    link_font = Font(color="0563C1", underline="single")
    for series, model, avg, count, url in rows:
        r = ws.max_row + 1
        ws.cell(row=r, column=1, value=series)
        ws.cell(row=r, column=2, value=model)
        ws.cell(row=r, column=3, value=avg)            # None → 빈 셀
        ws.cell(row=r, column=4, value=count)
        link_cell = ws.cell(row=r, column=5, value=url)
        link_cell.hyperlink = url
        link_cell.font = link_font

    # 숫자 천단위 콤마 서식
    for r in range(2, ws.max_row + 1):
        ws.cell(row=r, column=3).number_format = "#,##0"
        ws.cell(row=r, column=4).number_format = "#,##0"

    # 컬럼 너비
    widths = [10, 22, 12, 12, 70]
    for i, w in enumerate(widths, start=1):
        ws.column_dimensions[get_column_letter(i)].width = w

    ws.freeze_panes = "A2"
    ws.auto_filter.ref = f"A1:E{ws.max_row}"

    wb.save(path)
    # 신규 파일을 만들지 않고 '기존 파일 업데이트(날짜만 갱신)' 효과:
    # 같은 폴더의 이전 결과 xlsx 는 삭제해 항상 1개만 유지한다.
    for old in d.glob("3dmark_*_scores_*.xlsx"):
        if old != path:
            try:
                old.unlink()
            except Exception:  # noqa: BLE001
                pass
    return path


