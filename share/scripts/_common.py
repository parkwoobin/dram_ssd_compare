# URL 생성
# ---------------------------------------------------------------------------
def build_url(gpu_id: int, test_code: str = "spy P") -> str:
    """
    gpuId / 벤치마크에 대한 Graphics score 검색 URL 을 만든다.
    검색 조건은 hash(#advanced?...) 뒤에 들어가야 SPA 가 인식한다.
    """
    params = [
        ("test", test_code),
        ("cpuId", ""),
        ("gpuId", str(gpu_id)),
        ("gpuCount", "1"),
        ("gpuType", "ALL"),
        ("deviceType", "ALL"),
        ("storageModel", "ALL"),
        ("modelId", ""),
        ("showRamDisks", "false"),
        ("memoryChannels", "0"),
        ("country", ""),
        ("scoreType", "graphicsScore"),
        ("hofMode", "false"),
        ("showInvalidResults", "false"),
        ("freeParams", ""),
        ("minGpuCoreClock", ""),
        ("maxGpuCoreClock", ""),
        ("minGpuMemClock", ""),
        ("maxGpuMemClock", ""),
        ("minCpuClock", ""),
        ("maxCpuClock", ""),
    ]
    return f"{BASE}#advanced?{urlencode(params, safe='')}"


# ---------------------------------------------------------------------------
# 백엔드 통계 API 직접 호출
# ---------------------------------------------------------------------------
# 페이지 전체를 로드하는 대신 점수 통계 ajax 만 호출해 GPU 당 요청 수를 크게 줄인다.
#   - 평균(Average score) = medianscore API 의 median 필드
#   - 결과 수(Found N)     = newsearch  API 의 resultCount 필드
#   - scoreType=graphicsScore 가 URL 파라미터로 바로 적용돼 Graphics 기준이 된다.
# jQuery AJAX 와 동일한 Accept 헤더가 없으면 서버가 406 을 주므로 반드시 지정한다.
# (페이지 컨텍스트 안에서 fetch 하므로 쿠키/Origin 이 자동 적용되어 차단을 회피한다.)
_AJAX_HEADERS = {
    "Accept": "application/json, text/javascript, */*; q=0.01",
    "X-Requested-With": "XMLHttpRequest",
}


def _ajax_query(gpu_id: int, test_code: str = "spy P") -> str:
    return (f"test={test_code}&cpuId=&gpuId={gpu_id}&gpuCount=1&gpuType=ALL&deviceType=ALL"
            "&storageModel=ALL&modelId=&showRamDisks=false&memoryChannels=0&country="
            "&scoreType=graphicsScore&hofMode=false&showInvalidResults=false&freeParams="
            "&minGpuCoreClock=&maxGpuCoreClock=&minGpuMemClock=&maxGpuMemClock="
            "&minCpuClock=&maxCpuClock=")


_FETCH_STATS_JS = """async ({query, headers}) => {
    const base = 'https://www.3dmark.com/proxycon/ajax/';
    const r1 = await fetch(base + 'newsearch?' + query, {headers});
    if ([403, 429, 503].includes(r1.status)) return {blocked: r1.status};
    const j1 = await r1.json();
    const r2 = await fetch(base + 'medianscore?' + query, {headers});
    if ([403, 429, 503].includes(r2.status)) return {blocked: r2.status};
    const j2 = await r2.json();
    return {count: j1.resultCount, median: j2.median};
}"""


async def fetch_score(page, gpu_id: int, test_code: str = "spy P", *, retries: int = 2,
                      log: "logging.Logger | None" = None
                      ) -> "tuple[int | None, int | None, str]":
    """
    세션이 확보된 page 에서 통계 API(ajax)만 호출해
    (Graphics score 평균, 총 결과 수, 사용자용 URL) 을 반환한다.
    결과 0 건이면 (None, 0, url). 완전 실패 시 (None, None, url).
    """
    log = log or logging.getLogger("3dmark")
    url = build_url(gpu_id, test_code)
    query = _ajax_query(gpu_id, test_code)

    for attempt in range(1, retries + 2):
        try:
            res = await page.evaluate(_FETCH_STATS_JS,
                                      {"query": query, "headers": _AJAX_HEADERS})
            if res.get("blocked"):
                raise BlockedError(f"HTTP {res['blocked']}")

            count = res.get("count")
            median = res.get("median")
            if not count:                       # 0 또는 None → 결과 없음
                return None, (count or 0), url
            avg = round(median) if median is not None else None
            return avg, count, url
        except BlockedError:
            raise
        except Exception as e:  # noqa: BLE001
            log.warning(f"  gpuId={gpu_id}: 오류 {type(e).__name__}: {e} (시도 {attempt})")
            if attempt <= retries:
                await asyncio.sleep(2)

    return None, None, url


