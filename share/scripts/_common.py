def get_logger(kind: str, bench: str = "timespy") -> logging.Logger:
    # Windows cp949 콘솔에서 em dash(—) 등 미지원 문자로 죽지 않도록 안전 처리
    try:
        sys.stdout.reconfigure(errors="replace")
    except Exception:  # noqa: BLE001
        pass

    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    log_path = bench_dir(kind, bench) / f"run_{ts}.log"

    logger = logging.getLogger(f"3dmark.{kind}.{bench}")
    logger.setLevel(logging.INFO)
    logger.handlers.clear()

    fmt = logging.Formatter("%(asctime)s  %(levelname)-7s  %(message)s", "%H:%M:%S")

    sh = logging.StreamHandler(sys.stdout)
    sh.setFormatter(fmt)
    logger.addHandler(sh)

    fh = logging.FileHandler(log_path, encoding="utf-8")
    fh.setFormatter(fmt)
    logger.addHandler(fh)

    logger.info(f"로그 파일: {log_path}")
    return logger
